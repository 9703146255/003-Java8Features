package in.thiru.files1;

public class Book {
	String title;
	String author;
	Double price;
	public String getTitle() {
		return title;
	}
	public String getAuthor() {
		return author;
	}
	public Double getPrice() {
		return price;
	}
	public Book(String title, String author, Double price) {
		super();
		this.title = title;
		this.author = author;
		this.price = price;
	}
	
	
	

}
